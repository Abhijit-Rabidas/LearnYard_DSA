// C++ program to add two numbers

#include <bits/stdc++.h>
using namespace std;

int main()
{
    int num1, num2;
    cin >> num1 >> num2;

    int sum = num1 + num2;
    cout << "\nSum of " << num1 << " + " << num2 << " = " << sum;

    return 0;
}

